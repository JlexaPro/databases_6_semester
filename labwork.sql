DROP TABLE IF EXISTS contract;
DROP TABLE IF EXISTS employee;
DROP TABLE IF EXISTS client;
DROP TABLE IF EXISTS tour;


CREATE TABLE employee (
	employee_id INT PRIMARY KEY NOT NULL,
	full_name VARCHAR(50) NOT NULL,
	post VARCHAR(20) NOT NULL,
	date_of_hiring DATE NOT NULL,
	birthday DATE NOT NULL,
	phone VARCHAR(11) NOT NULL,
	address VARCHAR(50) NOT NULL,
	salary INT NOT NULL
	);

CREATE TABLE client (
	client_id INT PRIMARY KEY NOT NULL,
	full_name VARCHAR(50) NOT NULL,
	passport VARCHAR(11) NOT NULL,
	place_of_work VARCHAR(50) NOT NULL,
	phone VARCHAR(11) NOT NULL
	);
	
CREATE TABLE tour (
	tour_id INT PRIMARY KEY NOT NULL,
	country VARCHAR(15) NOT NULL,
	duration SMALLINT NOT NULL,
	tour_price NUMERIC NOT NULL,
	tour_description TEXT NOT NULL
	);	
	
	
CREATE TABLE contract (
	contract_id INT PRIMARY KEY NOT NULL,
	fk_employee_id INT REFERENCES employee(employee_id),
	fk_client_id INT REFERENCES client(client_id),
	fk_tour_id INT REFERENCES tour(tour_id),
	tour_start_date DATE NOT NULL,
	payment BOOLEAN
	);
	
	
